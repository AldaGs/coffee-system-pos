// Customer-facing menu. Read-only. No auth, no settings, no admin.
//
// Hits the Supabase get_active_menu() RPC (migration 015) with the
// publishable key. The resolver picks the right menu server-side based on
// priority + scheduled time windows (shop-local), with the catalog 'live'
// menu as always-on fallback. We re-fetch every 5 minutes so a scheduled
// menu swap (e.g. brunch → lunch at 11am) appears without restart.
//
// Response envelope (sanitized — internal fields never reach the client):
//   { menu: {id, kind, name, data}, shop, categories, modifier_groups }
// For kind='live' we render categories/modifier_groups (catalog view).
// For kind in ('pdf','image','designed') we render from menu.data — those
// renderers ship in later phases.
//
// Routed at /menu and bypasses every App.jsx gate. Two URL formats:
//
//   Short (preferred): /menu?p=PROJECT_REF
//     Derives Supabase URL from the ref, fetches the anon key from the
//     public menu bucket's config.json. Produces ~55-char QR-friendly URLs.
//
//   Legacy: /menu?u=BASE64_URL&k=BASE64_KEY
//     The shop's Supabase URL and publishable key arrive as base64-encoded
//     query params (generated by older MenuShareCard versions). Still
//     supported for backwards compatibility.

import { useEffect, useMemo, useRef, useState } from 'react';
import { createClient } from '@supabase/supabase-js';
import { formatForDisplay } from '../utils/moneyUtils';
import { applyTheme, syncGoogleFontLink } from '../utils/menuTheme';
import CanvasRenderer from './menuCanvas/CanvasRenderer';

// Standard base64 → string. Tolerates URL-safe variants in case the link was
// rewritten by a QR scanner or messaging app.
function decodeParam(value) {
  if (!value) return '';
  try {
    const normalized = value.replace(/-/g, '+').replace(/_/g, '/');
    return atob(normalized);
  } catch {
    return '';
  }
}

function PublicMenu() {
  const [data, setData] = useState(null);
  const [error, setError] = useState(null);
  const [activeCategoryId, setActiveCategoryId] = useState(null);
  const intervalRef = useRef(null);
  const isTvMode = typeof window !== 'undefined' && window.location.pathname === '/menu/tv';
  const isPrintMode = typeof window !== 'undefined' && new URLSearchParams(window.location.search).get('print') === '1';

  useEffect(() => {
    const params = new URLSearchParams(window.location.search);
    const projectRef = params.get('p');

    // Short-URL path: ?p=PROJECT_REF. Derives the Supabase URL from the ref
    // and fetches the anon key from the public menu bucket's config.json.
    if (projectRef) {
      const supabaseUrl = `https://${projectRef}.supabase.co`;
      const configUrl = `${supabaseUrl}/storage/v1/object/public/menu/config.json`;

      let cancelled = false;
      (async () => {
        try {
          const res = await fetch(configUrl);
          if (!res.ok) throw new Error('Config not found');
          const config = await res.json();
          const publishableKey = config.k;
          if (!publishableKey) throw new Error('Missing key in config');
          if (cancelled) return;

          const client = createClient(supabaseUrl, publishableKey, {
            auth: { persistSession: false, autoRefreshToken: false }
          });

          const pinnedId = params.get('m');
          async function fetchMenu() {
            const { data, error } = pinnedId
              ? await client.rpc('get_menu_by_id', { p_id: Number(pinnedId) })
              : await client.rpc('get_active_menu');
            if (cancelled) return;
            if (error) { setError(error.message); return; }
            setData(data);
            setActiveCategoryId(prev => {
              if (prev && data?.categories?.some(c => c.id === prev)) return prev;
              return data?.categories?.[0]?.id ?? null;
            });
          }
          fetchMenu();
          const iv = setInterval(fetchMenu, 5 * 60 * 1000);
          // Stash cleanup ref so the outer return can clear it.
          intervalRef.current = iv;
        } catch (err) {
          if (!cancelled) setError(err.message || 'Failed to load menu config');
        }
      })();
      return () => {
        cancelled = true;
        if (intervalRef.current) clearInterval(intervalRef.current);
      };
    }

    // Legacy long-URL path: ?u=BASE64_URL&k=BASE64_KEY (backwards compat).
    const supabaseUrl = decodeParam(params.get('u'));
    const publishableKey = decodeParam(params.get('k'));

    if (!supabaseUrl || !publishableKey) {
      setError('missing-config');
      return;
    }

    const client = createClient(supabaseUrl, publishableKey, {
      auth: { persistSession: false, autoRefreshToken: false }
    });

    // ?m=<id> pins a specific menu (per-card share links), bypassing the
    // schedule resolver. Falls back to get_active_menu when absent.
    const pinnedId = params.get('m');
    let cancelled = false;
    async function fetchMenu() {
      const { data, error } = pinnedId
        ? await client.rpc('get_menu_by_id', { p_id: Number(pinnedId) })
        : await client.rpc('get_active_menu');
      if (cancelled) return;
      if (error) { setError(error.message); return; }
      setData(data);
      setActiveCategoryId(prev => {
        if (prev && data?.categories?.some(c => c.id === prev)) return prev;
        return data?.categories?.[0]?.id ?? null;
      });
    }
    fetchMenu();
    // Re-resolve every 5 min so scheduled swaps appear without a manual refresh.
    const iv = setInterval(fetchMenu, 5 * 60 * 1000);
    return () => { cancelled = true; clearInterval(iv); };
  }, []);

  const groupsById = useMemo(() => {
    const m = new Map();
    (data?.modifier_groups || []).forEach(g => m.set(g.id, g));
    return m;
  }, [data]);

  if (error === 'missing-config') return <ConfigMissing />;
  if (error) return <ErrorState message={error} />;
  if (!data) return <LoadingState />;

  const shop = data.shop || {};
  const brand = shop.brand_color || '#f28b05';
  const lang = shop.language || 'es';
  const menuKind = data.menu?.kind || 'live';
  // Live mode shows out-of-stock items with an "Agotado" badge (rather than
  // hiding them) so a coffee list can advertise the whole lineup and its
  // freshness. Owners who prefer the old hide-when-sold-out behavior can set
  // menu.data.hide_out_of_stock = true on the menu row.
  const hideOos = data.menu?.data?.hide_out_of_stock === true;
  const categories = (data.categories || [])
    .map(c => ({ ...c, items: hideOos ? c.items.filter(it => it.available !== false) : c.items }));
  const activeCategory = categories.find(c => c.id === activeCategoryId) || categories[0];
  // Modifier groups show under each item by default; owners can hide them per
  // menu via menu.data.show_modifiers (set in the menu editor).
  const showModifiers = data.menu?.data?.show_modifiers !== false;

  if (isTvMode) {
    return <TvMode data={data} brand={brand} lang={lang} />;
  }

  // Designer kind:
  //   - menu.data.document present → freeform canvas (Phase 4c) renders it.
  //   - otherwise → template-mode (Phase 4a) renders categories via the
  //     chosen template + theme.
  if (menuKind === 'designed') {
    if (data.menu?.data?.document) {
      if (isPrintMode) {
        return <PrintWrapper enabled>
          <CanvasRenderer document={data.menu.data.document} data={data} lang={lang} isPrint />
        </PrintWrapper>;
      }
      // The global `body { overflow: hidden }` (POS anti-bounce in index.css)
      // would otherwise trap the page stack, so the canvas renderer needs its
      // own scroll container for multi-page / tall (9:16) documents.
      return (
        <div className="tinymenu-scroll" style={{ height: '100dvh', overflowY: 'auto', overflowX: 'hidden', background: '#0a0a0a', WebkitOverflowScrolling: 'touch' }}>
          <CanvasRenderer document={data.menu.data.document} data={data} lang={lang} />
        </div>
      );
    }
    return <TemplatedMenu data={data} brand={brand} lang={lang} />;
  }

  // Uploaded PDF/image kinds render as a page carousel from menu.data.pages.
  if (menuKind === 'pdf' || menuKind === 'image') {
    return (
      <div className="tinymenu-scroll" style={pageStyle}>
        <header style={{ ...headerStyle, background: brand }}>
          <h1 style={headerTitleStyle}>{shop.name || 'Menu'}</h1>
        </header>
        <UploadedMenu menu={data.menu} brand={brand} />
      </div>
    );
  }

  // kind='designed' renderer ships in Phase 4. Placeholder for now.
  if (menuKind !== 'live') {
    return (
      <div className="tinymenu-scroll" style={pageStyle}>
        <header style={{ ...headerStyle, background: brand }}>
          <h1 style={headerTitleStyle}>{shop.name || 'Menu'}</h1>
        </header>
        <div style={centerStyle}>
          <div>
            <p style={{ margin: 0, fontWeight: 700 }}>{data.menu?.name || 'Menú'}</p>
            <p style={{ marginTop: 8, fontSize: '0.85rem', color: '#888' }}>
              Este formato de menú ({menuKind}) aún no está disponible para mostrar.
            </p>
          </div>
        </div>
      </div>
    );
  }

  return (
    <div className="tinymenu-scroll" style={pageStyle}>
      <header style={{ ...headerStyle, background: brand }}>
        <h1 style={headerTitleStyle}>{shop.name || 'Menu'}</h1>
      </header>

      {categories.length === 0 ? (
        <EmptyState />
      ) : (
        <>
          <nav style={navStyle} aria-label="Categorías">
            {categories.map(c => {
              const isActive = c.id === activeCategory?.id;
              return (
                <button
                  key={c.id}
                  onClick={() => setActiveCategoryId(c.id)}
                  style={{
                    ...navButtonStyle,
                    color: isActive ? brand : '#555',
                    borderBottomColor: isActive ? brand : 'transparent'
                  }}
                >
                  {c.name}
                </button>
              );
            })}
          </nav>

          <main style={mainStyle}>
            {activeCategory && (
              <ItemList
                items={activeCategory.items}
                groupsById={groupsById}
                lang={lang}
                brand={brand}
                showModifiers={showModifiers}
              />
            )}
          </main>
        </>
      )}
    </div>
  );
}

function ItemList({ items, groupsById, lang, brand, showModifiers = true }) {
  if (!items || items.length === 0) {
    return <p style={{ textAlign: 'center', color: '#888', padding: '40px 16px' }}>—</p>;
  }
  return (
    <ul style={listStyle}>
      {items.map(item => {
        const soldOut = item.available === false;
        return (
        <li key={item.id} style={{ ...itemStyle, opacity: soldOut ? 0.55 : 1 }}>
          <div style={itemLeftStyle}>
            {item.image_url ? (
              <img
                src={item.image_url}
                alt=""
                loading="lazy"
                style={{ ...imageStyle, filter: soldOut ? 'grayscale(1)' : 'none' }}
              />
            ) : (
              <span style={emojiStyle} aria-hidden>{item.emoji || '•'}</span>
            )}
            <div>
              <div style={itemNameStyle}>
                {item.name}
                {soldOut && <span style={soldOutBadgeStyle}>{lang === 'en' ? 'Sold out' : 'Agotado'}</span>}
              </div>
              <RoastLine roastDate={item.roast_date} lang={lang} />
              {showModifiers && item.modifier_group_ids?.length > 0 && (
                <ItemModifiers groupIds={item.modifier_group_ids} groupsById={groupsById} lang={lang} brand={brand} />
              )}
              {item.whatsapp_url && (
                <WhatsAppButton url={item.whatsapp_url} lang={lang} />
              )}
            </div>
          </div>
          <div style={priceStyle}>
            {item.price_type === 'open'
              ? <span style={{ color: '#888', fontWeight: 600 }}>—</span>
              : formatForDisplay(item.price_cents, lang)}
          </div>
        </li>
        );
      })}
    </ul>
  );
}

// Roast-date line — shows the date plus a relative freshness hint ("hace 3
// días") because that's what a coffee buyer actually cares about. Renders
// nothing for a missing/unparseable date so non-coffee items stay clean.
function RoastLine({ roastDate, lang }) {
  const label = formatRoastDate(roastDate, lang);
  if (!label) return null;
  return (
    <div style={{ fontSize: '0.78rem', color: '#8a6d3b', fontWeight: 600, marginTop: 3 }}>
      🔥 {label}
    </div>
  );
}

function formatRoastDate(value, lang) {
  if (!value) return null;
  const d = new Date(`${value}T00:00:00`);
  if (Number.isNaN(d.getTime())) return null;
  const en = lang === 'en';
  const dateStr = d.toLocaleDateString(en ? 'en-US' : 'es-MX', { day: 'numeric', month: 'short', year: 'numeric' });
  const today = new Date();
  const days = Math.floor((new Date(today.getFullYear(), today.getMonth(), today.getDate()) - new Date(d.getFullYear(), d.getMonth(), d.getDate())) / 86400000);
  const roasted = en ? 'Roasted' : 'Tostado';
  if (days <= 0) return `${roasted}: ${dateStr} · ${en ? 'today' : 'hoy'}`;
  const ago = en ? `${days} day${days === 1 ? '' : 's'} ago` : `hace ${days} día${days === 1 ? '' : 's'}`;
  return `${roasted}: ${dateStr} · ${ago}`;
}

